package com.ishak;

public class Primitives {
	public static void main(String[] args) {

		// Primitive means Cannot be changes
		int enrollno = 121;
		char letter = 'i';
		float marks = 86.5f;
		double largeDecimal = 123.65645;
		long largeIntVals = 1232342534534L;
		boolean check = true;

		System.out.println(enrollno);
		System.out.println(letter);
		System.out.println(marks);
		System.out.println(largeDecimal);
		System.out.println(largeIntVals);
		System.out.println(check);

		// Non Primitive
		String name = "Ishak";
		System.out.println(name);
		System.out.println(name.charAt(1));

		// Literals And Identifiers
		// & Datatype Identifier = Literals
	}
}
